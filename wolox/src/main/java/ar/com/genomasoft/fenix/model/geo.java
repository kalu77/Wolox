package ar.com.genomasoft.fenix.model;

public class geo {

	String lat;
    String lng;
    
    public geo() {
	}
    
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
    
    
}
